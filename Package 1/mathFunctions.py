import math #Importing math library for functionalities unavailable in base python.

pi = 3.14
x = 1
y = 2
z = 3

print(round(pi)) #Function rounds variable up or down depending on the value.
print(math.ceil(pi)) #Function rounds the number up to the closest integer.
print(math.floor(pi)) #Function rounds the number down to the closest integer.
print(abs(pi)) #Function calculates how far the number is from zero.
print(pow(pi,2)) #Function calculates power of the variable (variable,power).
print(math.sqrt(420)) #Function calculates square root of the number.
print(max(x,y,z)) #Function looks for the biggest value assigned to variables.
print(min(x,y,z)) #Function looks for the lowest value assigned to variables.